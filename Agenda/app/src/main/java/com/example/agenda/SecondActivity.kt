@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun Evenement(innerPadding: PaddingValues){

    var nom by remember { mutableStateOf("") }
    var desc by remember { mutableStateOf("") }

    val date = remember { mutableStateOf("") }

    val ctx = LocalContext.current

    Column(
        modifier = Modifier.padding(innerPadding).padding(16.dp)
    ) {

        DatePickerFieldToModal(modifier = Modifier,date)

        OutlinedTextField(
            value = nom,
            onValueChange = {
                nom = it
            },
            label = {
                Text("Nom de l'évènement")
            },
            modifier = Modifier.fillMaxWidth()
        )
        OutlinedTextField(
            value = desc,
            onValueChange = {
                desc = it
            },
            label = {
                Text("Description de l'évènement")
            },
            modifier = Modifier.fillMaxWidth()
        )
        Button(
            onClick = {
                val intentBack = Intent().apply {
                    putExtra("date",date.value)
                    putExtra("nom",nom)
                    putExtra("desc",desc)
                }

                (ctx as? ComponentActivity)?.apply{
                    setResult(RESULT_OK, intentBack)
                    finish()
                }
            }
        ) {
            Text("Ajouter")
        }
    }
}

